    local -i time=0
    local -i newTime
    action=
    while true; do
        key=""
        read -t $TIMEOUT -n 1 key
        newTime=$((`date +%s` * 100 + (10#`date +%N` / 10000000)))
        case "$key" in
            $UP_KEY)	action='moveForward';;
            $DOWN_KEY)	action='moveBack';;
            $LEFT_KEY)  action='turnLeft';;
            $RIGHT_KEY) action='turnRight';;
            $FIRE_KEY)  action='fire';;
            "")		;;
        esac
        if [[ $((newTime - time)) -ge DELAY ]]; then
            needPrint=false
            MoveMonster
            MovePlayer
            if $needPrint; then
                MoveShot
                PrintScreen
                echo "$monsterX $monsterY"
            fi
            action=
            time=newTime
        fi
    done
}


