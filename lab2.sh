check(){
    local x=$1
    local y=$2
    local dir=$3
    let "x += ${DIR_X[${dir}]}"
    let "y += ${DIR_Y[${dir}]}"
    if [[ $x -lt 1 || $y -lt 1 || $x -gt $(( $LABIRINTH_HEIGHT - 2 )) || $y -gt $(( $LABIRINTH_WIDTH - 2 )) ]]; then
        return 1
    fi
    if [[ ${labirinth[$(( $x * $LABIRINTH_WIDTH + $y ))]} -eq 0 ]]; then
        return 1
    fi
    if [[ ${labirinth[$(( (x + DIR_X[$dir]) * LABIRINTH_WIDTH + y + DIR_Y[$dir] )) ]} -eq 0 ]]; then
        return 1
    fi
    local dx=(-1 -1 0 0)
    local dy=(-1 0 -1 0)
    for (( i=0; i < 4; ++i )); do
        cnt=0
        for (( j=0; j < 4; ++j )); do
            let "cnt += ${labirinth[(x + dx[i] + j % 2) *  LABIRINTH_WIDTH + y + dy[i] + j / 2]}"
        done
        if [[ $cnt -eq 1 ]]; then
            return 1
        fi
    done
    return 0
}

ended() {
    for (( ei=0; ei < 4; ++ei )); do
        if check $1 $2 $ei; then
            return 1
        fi
    done
    return 0
}


GenerateLabirinth() {
